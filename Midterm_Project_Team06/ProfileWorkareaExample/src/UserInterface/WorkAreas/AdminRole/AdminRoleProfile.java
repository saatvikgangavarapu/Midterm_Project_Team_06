    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (currentUser == null) {
            JOptionPane.showMessageDialog(this, "No logged-in user found.");
            return;
        }

        String newUsername = txtUsername.getText().trim();
        String newPassword = txtPassword.getText().trim();
        String contactId = txtEmail.getText().trim();

        if (newUsername.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username is required.");
            return;
        }
        
        Profile prof = currentUser.getAssociatedPersonProfile();
        if (prof != null) {
            Person p = prof.getPerson();
            if (p != null) p.setContactId(contactId);
        }
        if (contactId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is required.");
            return;
        }
        if (!contactId.contains("@")) {
            JOptionPane.showMessageDialog(this, "Enter a valid email.");
            return;
        }
        UserAccountDirectory uad = business.getUserAccountDirectory();
        String oldUsername = currentUser.getUserLoginName();
        boolean changed = !oldUsername.equalsIgnoreCase(newUsername);

        if (changed && uad.isUsernameTaken(newUsername)) {
            JOptionPane.showMessageDialog(this, "Username already exists.");
            return;
        }
        currentUser.setUserLoginName(newUsername);
        
        if (!newPassword.isEmpty()) {
            currentUser.setPassword(newPassword);
        }
        
        JOptionPane.showMessageDialog(this, "Profile updated.");
        populateFields();
    }//GEN-LAST:event_btnUpdateActionPerformed


