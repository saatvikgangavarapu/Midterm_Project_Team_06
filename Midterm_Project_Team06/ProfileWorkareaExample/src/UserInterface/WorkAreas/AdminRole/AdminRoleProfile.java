    private void populateFields() {
        if (currentUser == null) return;

        txtPerson.setText(currentUser.getPersonId());
        txtUsername.setText(currentUser.getUserLoginName());
        txtPassword.setText("");

        Profile prof = currentUser.getAssociatedPersonProfile();
        if (prof != null && prof.getPerson() != null) {
            txtEmail.setText(prof.getPerson().getContactId());
        } else {
            txtEmail.setText("");
        }

        txtPerson.setEditable(false);
    }
    
    
